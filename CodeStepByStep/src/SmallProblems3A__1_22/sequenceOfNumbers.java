package SmallProblems3A__1_22;

/*
 * 
 * Complete a for loop to produce the following output:
 * 
 * 8
 * 11
 * 14
 * 17
 * 20
 * 23
 * 
 */

public class sequenceOfNumbers {
    public static void main (String[] args) {
        for (int i = 8; i <= 23; i += 3) {
            System.out.println(i);
        }
    }
}
